    public BillDetailFrm(String time, User user){
        super("List Bill");
        this.user= user;
        this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        this.setLocation(400,170);
        initComponents();
        lblmonth.setText("Time: "+time);

        ArrayList<Bill> push = new BillDAO().getBillByMonth(time);
        String[][] value = new String[push.size()][8];
        String[] columnNames = {"ID", "Name","RentDay","ReturnDay","price","deposit","Into Money","Total Amount"};
        NumberFormat nf = NumberFormat.getNumberInstance();
        nf.setGroupingUsed(true);
        for (int i = 0; i < push.size(); i++) {
            value[i][0] = String.valueOf(push.get(i).getID());
            value[i][1] = push.get(i).getBooking().getClient().getFullname();
            value[i][2] = String.valueOf(push.get(i).getBooking().getRentDay());
            value[i][3] = String.valueOf(push.get(i).getPaymentDay());
            float price = (float) Math.ceil(Double.parseDouble(String.valueOf(push.get(i).getBooking().getRentedmotorbikes().get(0).getPrice())));
//            System.out.println(push.get(i).getBooking().getRentedmotorbikes().size());
            value[i][4] = nf.format(price);
            float deposit = (float) Math.ceil(Double.parseDouble(String.valueOf(push.get(i).getBooking().getDeposits().get(0).getValue())));
            value[i][5] = nf.format(deposit);
            float intomoney = (float) Math.ceil(Double.parseDouble(String.valueOf(push.get(i).getBooking().getIntoMoney())));
            value[i][6] = nf.format(intomoney);
            float amount = (float) Math.ceil(Double.parseDouble(String.valueOf(push.get(i).getBooking().getTotal())));
            value[i][7] = nf.format(amount);
        }
        DefaultTableModel tableModel = new DefaultTableModel(value, columnNames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblbill.setModel(tableModel);

    }

