public class Deposit {
    int ID;
    double value;
    String note;

    public Deposit() {
    }

    public Deposit(int ID, double value, String note) {
        this.ID = ID;
        this.value = value;
        this.note = note;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
}
